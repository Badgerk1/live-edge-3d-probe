// ── Test 5: Feed rates are preserved across segments ────────────────────────

function testFeedRatePreservation() {
  console.log('\nTest: Feed rates are preserved across segmented moves');

  const gcode = 'G90\nG1 X0 Y0 F200\nG1 X20 Y0 Z-1 F150';
  const mesh = createFlatMesh();
  const gridParams = createGridParams();
  const referenceZ = 0;

  const result = applyZCompensation(gcode, mesh, gridParams, referenceZ);
  const lines = result.split('\n');

  const g1Lines = lines.filter(l => l.startsWith('G1 X'));

  // First segment of subdivided move should have F150
  assertContains(g1Lines[g1Lines.length - 10] || '', 'F150', 'First segment should have updated feed rate');
}

// ── Test 6: Comments and non-motion lines are preserved ─────────────────────


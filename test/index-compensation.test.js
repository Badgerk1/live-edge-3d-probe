// ── Test 7: Relative mode moves pass through unchanged ──────────────────────

function testRelativeModeMoves() {
  console.log('\nTest: Relative mode (G91) moves pass through unchanged');

  const gcode = 'G90\nG1 X10 Y10 Z0\nG91\nG1 X5 Y5\nG90';
  const mesh = createFlatMesh();
  const gridParams = createGridParams();
  const referenceZ = 0;

  const result = applyZCompensation(gcode, mesh, gridParams, referenceZ);

  // G91 move should pass through without compensation
  assertContains(result, 'G91', 'G91 mode switch should be preserved');

  const lines = result.split('\n');
  const g91Index = lines.findIndex(l => l.includes('G91'));
  const nextLine = lines[g91Index + 1];

  // The relative move should not be compensated (it will pass through as-is)
  assert(nextLine.includes('G1 X5 Y5'), 'Relative move should pass through unchanged');
}


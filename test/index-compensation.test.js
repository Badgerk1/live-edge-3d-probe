// ── Test 11: Mixed Z values with compensation ────────────────────────────────

function testMixedZValues() {
  console.log('\nTest: Mixed Z values are compensated correctly');

  const gcode = `
G90
G0 Z5
G1 X5 Y5 Z-1 F100
G1 Z-2
G1 X10 Y10
G0 Z5
  `.trim();

  const mesh = createTiltedMesh();
  const gridParams = createGridParams();
  const referenceZ = 0;

  const result = applyZCompensation(gcode, mesh, gridParams, referenceZ);
  const lines = result.split('\n');

  // Count how many lines have Z compensation
  const compensatedLines = lines.filter(l => l.includes('Z') && !l.startsWith('('));
  assert(compensatedLines.length > 0, 'Should have compensated Z values');
}

